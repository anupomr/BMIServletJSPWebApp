package bmi.exceptions;

public class ImpossibleWeightException extends Exception {
	private static final long serialVersionUID = 1L;

	public ImpossibleWeightException() {
		
	}

	public ImpossibleWeightException(String arg0) {
		super(arg0);
		
	}

	public ImpossibleWeightException(Throwable arg0) {
		super(arg0);
		
	}

	public ImpossibleWeightException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public ImpossibleWeightException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
